public class ejercicio01_tipos_datos {

    /**
     * @param args the command line arguments
     */
    public static void main(String args[]) {
        // 
        byte numero_byte = (byte) 253;
        long num_largo = 4565456783434543L;
        int entero = -5;
        float decimal = 0.876F;
        double doble = 8.76;
        char caracter = 'A';
        String cadena = "Ejercicio 1";
        System.out.println("La variable de tipo byte tiene el valor de: " + numero_byte);
        System.out.println("La variable de tipo long tiene el valor de: " + num_largo);
        System.out.println("La variable de tipo entero tiene el valor de: " + entero);
        System.out.println("La variable de tipo float tiene el valor de: " + decimal);
        System.out.println("La variable de tipo double tiene el valor de: " + doble);
        System.out.println("La variable de tipo char tiene el valor de: " + caracter);
        System.out.println("La variable de tipo string tiene el valor de: " + cadena);
        
    }
}
